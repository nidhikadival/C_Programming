/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : DisplayASCII
//  Description :   It displays the ASCII table with decimal, hexadecimal, octal, characters
//  Input :         - 
//  Output :        void
//  Author :        Nidhi Girish Kadival
//  Date :          28/11/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

void DisplayASCII()
{
    printf("Decimal\tOctal\tHex\tChar\n");

    for(int i = 0; i <= 255; i++)
    {
        if(i < 32 || i == 127)
        {
            printf("%d\t%o\t%X\t - \n", i, i, i);
        }    
        else
        {
            printf("%d\t%o\t%X\t %c \n", i, i, i, i);
        }  
    }
} // End of DisplayASCII

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    DisplayASCII();
    return 0;
}

