#include<stdio.h>
#include<stdbool.h>

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : Display
//  Description :   It displays capital letters as small and small as capital 
//  Input :         char
//  Output :        void
//  Author :        Nidhi Girish Kadival
//  Date :          28/11/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

void Display(char ch)
{
    if( (ch>='a' && ch<='z'))
    {
        ch = ch - 32;
    }
    else if( (ch>='A' && ch<='Z'))
    {
        ch = ch + 32;
    }
    printf("%c\n",ch);
}   // End of Display

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = {'\0'};

    printf("Enter character:\n");
    scanf("%c",&cValue);

    Display(cValue);

    return 0;
}

