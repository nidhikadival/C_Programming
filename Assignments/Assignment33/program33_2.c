#include<stdio.h>
#include<stdbool.h>

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : ChkCapital
//  Description :   It returns true if the input is a capital charcter 
//  Input :         char
//  Output :        bool
//  Author :        Nidhi Girish Kadival
//  Date :          28/11/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

bool ChkCapital(char ch)
{
    if((ch>='A' && ch<='Z'))
    {
        return true;
    }
    else
    {
        return false;
    }
}   // End of ChkCapital

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = {'\0'};
    bool bRet = false;

    printf("Enter character:\n");
    scanf("%c",&cValue);

    bRet = ChkCapital(cValue);

    if (bRet == true)
    {
        printf("It is a capital character\n");
    }
    else
    {
        printf("It is not a capital character\n");
    }

    return 0;
}

