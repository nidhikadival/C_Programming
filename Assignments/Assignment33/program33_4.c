#include<stdio.h>
#include<stdbool.h>

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : ChkSmall
//  Description :   It returns true if the input is a small case character
//  Input :         char
//  Output :        bool
//  Author :        Nidhi Girish Kadival
//  Date :          28/11/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

bool ChkSmall(char ch)
{
    if((ch>='a' && ch<='z'))
    {
        return true;
    }
    else
    {
        return false;
    }
}   // End of ChkSmall

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = {'\0'};
    bool bRet = false;

    printf("Enter character:\n");
    scanf("%c",&cValue);

    bRet = ChkSmall(cValue);

    if (bRet == true)
    {
        printf("It is a small case character \n");
    }
    else
    {
        printf("It is not a small case character \n");
    }

    return 0;
}

