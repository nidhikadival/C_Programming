#include<stdio.h>
#include<stdbool.h>

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : ChkDigit
//  Description :   It returns true if the input is a digit (0-9)
//  Input :         char
//  Output :        bool
//  Author :        Nidhi Girish Kadival
//  Date :          28/11/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

bool ChkDigit(char ch)
{
    if((ch>='0' && ch<='9'))
    {
        return true;
    }
    else
    {
        return false;
    }
}   // End of ChkDigit

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = {'\0'};
    bool bRet = false;

    printf("Enter character:\n");
    scanf("%c",&cValue);

    bRet = ChkDigit(cValue);

    if (bRet == true)
    {
        printf("It is a digit\n");
    }
    else
    {
        printf("It is not a digit\n");
    }

    return 0;
}

