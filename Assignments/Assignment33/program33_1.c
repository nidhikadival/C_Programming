#include<stdio.h>
#include<stdbool.h>

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : ChkAlpha
//  Description :   It returns true if the input is a charcter else it returns false
//  Input :         char
//  Output :        bool
//  Author :        Nidhi Girish Kadival
//  Date :          28/11/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

bool ChkAlpha(char ch)
{
    if( (ch>='a' && ch<='z')|| (ch>='A' && ch<='Z'))
    {
        return true;
    }
    else
    {
        return false;
    }
}   // End of ChkAlpha

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = {'\0'};
    bool bRet = false;

    printf("Enter character:\n");
    scanf("%c",&cValue);

    bRet = ChkAlpha(cValue);

    if (bRet == true)
    {
        printf("It is a character\n");
    }
    else
    {
        printf("It is not a character\n");
    }

    return 0;
}

