/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : struprX
//  Description :   It converts lower case string to upper case  
//  Input :         char *
//  Output :        void
//  Author :        Nidhi Girish Kadival
//  Date :          29/11/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

void struprX(char *str)
{
    while(*str != '\0')
    {
       if((*str>='a') && (*str<='z'))
       {
            *str = *str - 32;
       }
       str++;
    }
}   // End of struprX

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20] = {'\0'};

    printf("Enter string:\n");
    scanf("%[^'\n']s",Arr);

    struprX(Arr);

    printf("Updated string is: %s\n",Arr);

    return 0;
}

