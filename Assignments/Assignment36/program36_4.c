/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : DisplayDigit
//  Description :   It displays the digits in a given string
//  Input :         char *
//  Output :        void
//  Author :        Nidhi Girish Kadival
//  Date :          29/11/2025
// 
