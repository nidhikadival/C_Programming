void DisplayDigit(char *str)
{
    while(*str != '\0')
    {
       if((*str>='0') && (*str<='9'))
       {
            printf("%c\t",*str);
       }
       str++;
    }
    printf("\n");
}   // End of DisplayDigit

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20] = {'\0'};

    printf("Enter string:\n");
    scanf("%[^'\n']s",Arr);

    DisplayDigit(Arr);

    return 0;
}

