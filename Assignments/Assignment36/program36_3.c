/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : strtoggleX
//  Description :   It converts lower to upper case  and upper to lower case
//  Input :         char *
//  Output :        void
//  Author :        Nidhi Girish Kadival
//  Date :          29/11/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

void strtoggleX(char *str)
{
    while(*str != '\0')
    {
       if((*str>='a') && (*str<='z'))
       {
            *str = *str - 32;
       }
       else if((*str>='A') && (*str<='Z'))
       {
            *str = *str + 32;
       }
       str++;
    }
}   // End of strtoggleX

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20] = {'\0'};

    printf("Enter string:\n");
    scanf("%[^'\n']s",Arr);

    strtoggleX(Arr);

    printf("Updated string is: %s\n",Arr);

    return 0;
}

