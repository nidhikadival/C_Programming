/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : CountWhite
//  Description :   It counts the white spaces
//  Input :         char *
//  Output :        int
//  Author :        Nidhi Girish Kadival
//  Date :          29/11/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int CountWhite(char *str)
{
    int iCount = 0;

    while(*str != '\0')
    {
       if(*str==' ')
       {
            iCount++;
       }
       str++;
    }
    return iCount;
}   // End of CountWhite

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20] = {'\0'};
    int iRet = 0;

    printf("Enter string:\n");
    scanf("%[^'\n']s",Arr);

    iRet = CountWhite(Arr);

    printf("No of white spaces: %d\n",iRet);

    return 0;
}

