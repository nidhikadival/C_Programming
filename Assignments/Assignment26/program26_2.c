#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : Pattern
//  Description :   It displays numbers in reverse order with # symbol
//  Input :         int 
//  Output :        void
//  Author :        Nidhi Girish Kadival
//  Date :          21/11/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

void Pattern(int iNo)
{
    int iCnt = 0;

    for(iCnt=iNo; iCnt>=1; iCnt--)
    {
        printf("%d\t#\t",iCnt);
    }
    printf("\n");
}   //Pattern

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number of elements:\n");
    scanf("%d",&iValue);

    Pattern(iValue);

    return 0;
}

