#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : Pattern
//  Description :   It displays the alphabets for an number input
//  Input :         int 
//  Output :        void
//  Author :        Nidhi Girish Kadival
//  Date :          21/11/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

void Pattern(int iNo)
{
    int iCnt = 0;

    for(iCnt=0; iCnt<iNo; iCnt++)
    {
        printf("%c\t",'A'+iCnt);
    }
    printf("\n");
}   //Pattern

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number of elements:\n");
    scanf("%d",&iValue);

    Pattern(iValue);

    return 0;
}

