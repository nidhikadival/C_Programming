void StrCpyX(char *src, char *dest)
{
    while(*src != '\0')
    {
        if(*src != ' ')
        {
           *dest = *src; 
           dest++;
        }
        src++;
    }
    *dest = '\0';
}   // End of StrCpyX

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20] = {'\0'};
    char Brr[20] = {'\0'};

    printf("Enter string:\n");
    scanf("%[^'\n']s",Arr);

    StrCpyX(Arr,Brr);

    printf("Updated string is: %s\n",Brr);

    return 0;
}

