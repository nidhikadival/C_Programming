#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : CountChar
//  Description :   It returns the count of the given character
//  Input :         char *
//  Output :        int
//  Author :        Nidhi Girish Kadival
//  Date :          29/11/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int CountChar(char *str, char ch)
{
    int iCount = 0;

    while(*str != '\0')
    {
       if(*str==ch)
       {
            iCount++;
       }
       str++;
    }

    return iCount;
}   // End of CountChar

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20] = {'\0'};
    char ch = '\0';
    int iRet = 0;

    printf("Enter string:\n");
    scanf("%[^'\n']s",Arr);

    printf("Enter character:\n");
    scanf(" %c",&ch);

    iRet = CountChar(Arr,ch);

    printf("Character count is %d\n",iRet);

    return 0;
}

