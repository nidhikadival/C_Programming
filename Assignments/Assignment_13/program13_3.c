#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name   : PrintOddNumbers
//  Description     : It is used to print even numbers from 1 to N
//  Input           : Int
//  Output          : Void
//  Author          : Nidhi Girish Kadival
//  Date            : 2/11/2025
//  
//////////////////////////////////////////////////////////////////////////////////////////////////

void PrintOddNumbers(int iNo)
{
    int iCnt = 0;

    if (iNo < 0)
    {
        iNo = -iNo;
    }

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        if(iCnt % 2 != 0)
        {
            printf("%d\t", iCnt);
        }
    }
} // End of PrintOddNumbers

//  Time complexity :   O(N)

//////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
//////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number: ");
    scanf("%d", &iValue);

    PrintOddNumbers(iValue);
    
    return 0;
}

