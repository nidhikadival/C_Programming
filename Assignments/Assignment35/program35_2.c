#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : CountSmall
//  Description :   It returns the no of small letters in a given string
//  Input :         char *
//  Output :        int
//  Author :        Nidhi Girish Kadival
//  Date :          28/11/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int CountSmall(char *str)
{
    int iCount = 0;

    while(*str!='\0')
    {
        if((*str>='a' && *str<='z'))
        {
            iCount++;
        }
        str++;
    }
    return iCount;
}   // End of CountSmall

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20] = {'\0'};
    int iRet = 0;

    printf("Enter string:\n");
    scanf("%[^'\n']s",Arr);

    iRet = CountSmall(Arr);

    printf("The no of small letters are: %d\n",iRet);

    return 0;
}

