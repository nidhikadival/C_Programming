/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : Reverse
//  Description :   It displays the string in reverse 
//  Input :         char *
//  Output :        void
//  Author :        Nidhi Girish Kadival
//  Date :          28/11/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

void Reverse(char *src, char *dest)
{
    while(*src!='\0')
    {
        src++;
    }
    src--;
    while(*src != '\0')
    {
        *dest = *src;
        dest++;
        src--;
    }
    *dest = '\0';
}   // End of Reverse

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20] = {'\0'};
    char Brr[20] = {'\0'};

    printf("Enter string:\n");
    scanf("%[^'\n']s",Arr);

    Reverse(Arr,Brr);

    printf("Updated string is: %s\n",Brr);

    return 0;
}

