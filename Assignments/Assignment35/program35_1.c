#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : CountCapital
//  Description :   It returns the no of capital letters in a given string
//  Input :         char *
//  Output :        int
//  Author :        Nidhi Girish Kadival
//  Date :          28/11/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int CountCapital(char *str)
{
    int iCount = 0;

    while(*str!='\0')
    {
        if((*str>='A' && *str<='Z'))
        {
            iCount++;
        }
        str++;
    }
    return iCount;
}   // End of CountCapital

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20] = {'\0'};
    int iRet = 0;

    printf("Enter string:\n");
    scanf("%[^'\n']s",Arr);

    iRet = CountCapital(Arr);

    printf("The no of capital letters are: %d\n",iRet);

    return 0;
}

